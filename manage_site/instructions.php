<h1>To add a new page in Activities:</h1>
<h2><b>*should be used later in the step 5</b></h2>
<ol>
	<li><b><u>To add heading below nav bar and introductory para:</u></b><br> a. Go to homeinfo(3) tab;<br> b. Enter details;<br> c. <b>keyword</b>* will be the id for selection (Note it)</li>
	<li><b><u>If change in down link is required insert them:</u></b><br> a. Go to Vertical Nav(4) tab;<br> b. Enter details;<br> c. <b>Page_select</b>* will be the id for selection (Note it)</li>
	<li><b><u>To add box in Activities page:</u></b><br> a. Go to Actbox(8) tab;<br> b. Enter details;<br> c. There should be no spelling mistake in <b>A_href</b>;<br> d. Also make sure that the part after '=' sign is the same as <b>keyword</b> in homeinfo table</li>
	<li><b><u>To add Right content:</u></b><br> a. Go to Right content(10) tab;<br> b. Enter details;<br> c. <b>rgt_cnt_ind</b>* will be the id for selection (Note it);<br> d. <b>rgt_cnt_ord</b>* will be the order of selection(ascending order)</li>
	<li><b><u>The Final Step:</u></b><br> a. Go to Base page(9) tab;<br> b. Enter details;<br> c. <b>Home info</b>* will be the id for selection<br><b>Note: 'Down link' and 'Right content' are optional</b></li>
</ol>
<h1>Buttons in forms:</h1>
<ul>
	<li>Insert : to enter new data (the ID field is not required)</li>
	<li>Update: to edit the stored data in the database(ID is must, other fields optional, blank fields will delete related columns)</li>
	<li>Delete : to stored data(ID is must, other fields not required)</li>
</ul>
